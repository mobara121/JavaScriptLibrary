//equal(does not check for data type equality)
console.log("3" == 3);

//strict equal
console.log("3" === 3);

//not equal !3じゃないですよ。つまりfalse
console.log("3" != 3);

//strict not equal
console.log("3" === 3);

//gretaer than
console.log(3 > 1);

//less than
console.log(3 < 1);

//greater than or equal to
console.log(5 >= 3);

//less than or equal to
console.log(5 <=4);

//and
console.log(1 > 0 && 2 > 1);

//or
console.log(1 > 0 || 2 > 7);